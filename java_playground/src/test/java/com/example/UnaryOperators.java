package com.example;

public class UnaryOperators {
    public static void main(String[] args) {
        int a = 1;
        int b = 1;
        int c = ++a;
        int d = b++;
        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("c: " + c);
        System.out.println("d: " + d);
    }
}
